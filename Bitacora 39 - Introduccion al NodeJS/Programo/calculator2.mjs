export function sumar(a,b){
    let suma = a+b;
    return suma
}
export function restar(a,b){
    let resta = a-b;
    return resta
}
export function multiplicar(a,b){
    let multiplicacion = a*b;
    return multiplicacion
}
export function dividir(a,b){
    let division = a/b;
    return division
}